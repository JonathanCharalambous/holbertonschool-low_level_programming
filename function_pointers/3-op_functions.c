#include "3-calc.h"

/**
 * op_add- adds the two paramters
 * @a: 1st int
 * @b: 2nd int
 * Return: addition of the two ints
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub- subtracts the two paramters
 * @a: 1st int
 * @b: 2nd int
 * Return: difference of the two ints
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul- multiplies the two paramters
 * @a: 1st int
 * @b: 2nd int
 * Return: multiplication of the two ints
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div- divides the two paramters
 * @a: 1st int
 * @b: 2nd int
 * Return: division of the two ints
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod- modulus the two paramters
 * @a: 1st int
 * @b: 2nd int
 * Return: modulus of the two ints
 */

int op_mod(int a, int b)
{
	return (a % b);
}
